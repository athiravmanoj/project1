package pack;

public class Datatypes {

	public static void main(String[] args) 
	
			{
		//byte
		int a=10,z=10;
		int c1=a+z;
		
		System.out.println(c1);
		
		byte b=121;
		System.out.println("value of byte="+b);
		
		short s=2344;
		System.out.println("value of short="+s);
		
		int v=236789;
		System.out.println("value of int="+v);
		
		long l=5467980987l;
		System.out.println("value of long="+l);
		
		float f1=5.67f;
		System.out.println("value of float="+f1);
		
		double d=3.2324;
		System.out.println("value of double="+d);
		
		boolean b1=10>20;
		System.out.println("value of boolean="+b1);
		
		char c='A';
		System.out.println("value of character="+c);
		
		String s1="ASD";
		System.out.println("value of string="+s1);
		
	}

}
